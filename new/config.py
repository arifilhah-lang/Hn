def get_subject_group_folder(class_id, subject_id):
    """
    Subject টা কোন group folder এ যাবে সেটা বের করো।
    common group → 'universal'
    science/business/humanities → নিজের নাম
    Priority: subjects > 4th_subjects (duplicate হলে primary group জেতে)
    """
    cls = CLASSES.get(class_id, {})
    groups = cls.get("groups", {})
    # প্রথমে primary subjects এ খোঁজো
    for group_id, group in groups.items():
        if subject_id in group.get("subjects", {}):
            return "universal" if group_id == "common" else group_id
    # তারপর 4th_subjects এ খোঁজো
    for group_id, group in groups.items():
        if subject_id in group.get("4th_subjects", {}):
            return "universal" if group_id == "common" else group_id
    return "universal"


def get_data_filepath(class_id, subject_id, base_data_dir, source_type=None):
    """
    Folder structure:
      data/ssc/science/physics/board_book.json
      data/ssc/science/physics/test_paper.json
      data/ssc/science/physics/guide.json
    source_type না দিলে subject folder return করো।
    """
    import os
    group_folder = get_subject_group_folder(class_id, subject_id)
    folder = _os.path.join(base_data_dir, class_id, group_folder, subject_id)
    _os.makedirs(folder, exist_ok=True)
    if source_type:
        return _os.path.join(folder, f"{source_type}.json")
    return folder  # folder path — সব source_type একসাথে দরকার হলে


def find_data_file(class_id, subject_id, base_data_dir, source_type=None):
    """
    source_type দিলে নির্দিষ্ট file খোঁজো।
    না দিলে যেকোনো একটা available file return করো।
    Legacy flat path ও support করে।
    Returns: filepath (str) অথবা None
    """
    if source_type:
        # নির্দিষ্ট source_type খোঁজো
        new_path = get_data_filepath(class_id, subject_id, base_data_dir, source_type)
        if _os.path.exists(new_path):
            return new_path
        # Legacy: data/ssc_physics.json (source_type ছাড়া)
        legacy_path = _os.path.join(base_data_dir, f"{class_id}_{subject_id}.json")
        if _os.path.exists(legacy_path):
            return legacy_path
        return None

    # source_type নেই — যেকোনো available file দাও
    for src in ("board_book", "test_paper", "guide"):
        p = get_data_filepath(class_id, subject_id, base_data_dir, src)
        if _os.path.exists(p):
            return p
    # Legacy fallback
    legacy_path = _os.path.join(base_data_dir, f"{class_id}_{subject_id}.json")
    if _os.path.exists(legacy_path):
        return legacy_path
    return None


def find_all_data_files(class_id, subject_id, base_data_dir):
    """
    একটা subject এর সব available source_type files return করো।
    Returns: list of (source_type, filepath)
    """
    results = []
    for src in ("board_book", "test_paper", "guide"):
        p = get_data_filepath(class_id, subject_id, base_data_dir, src)
        if _os.path.exists(p):
            results.append((src, p))
    return results


