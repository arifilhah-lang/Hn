def get_extract_prompt(prompt_type: str, subject_id: str = "") -> str:
    """
    prompt_type (board_book / test_paper / guide) এবং subject_id মিলিয়ে
    subject-specific extraction prompt তৈরি করে return করে।

    Subject-specific rules সব prompt type এ যোগ হয় — তাই Physics এর LaTeX
    rule এখন TEST_PAPER ও GUIDE prompt এও কাজ করবে।

    ব্যবহার (admin_app.py তে):
        selected_prompt = get_extract_prompt(prompt_type, subject_id)
    """
    base = EXTRACT_PROMPTS.get(prompt_type, EXTRACT_PROMPT_BOARD_BOOK)
    group = _EXTRACT_GROUP.get(subject_id, "")
    subject_addon = _EXTRACT_SUBJECT_RULES.get(group, "")
    if subject_addon:
        return base + "\n\n" + subject_addon
    return base
