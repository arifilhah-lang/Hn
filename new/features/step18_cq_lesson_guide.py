import re

# (ক) (খ) (গ) (ঘ) — বাংলা bracket bullet detect করার pattern
_CQ_PART_PATTERN = re.compile(r'\((ক|খ|গ|ঘ)\)')

# কিছু সাধারণ keyword যা দিয়ে ছাত্র সরাসরি উত্তর চাইছে বোঝা যায়
_ANSWER_REQUEST_KEYWORDS = [
    "উত্তর দাও", "উত্তর লিখে দাও", "উত্তর করো", "সমাধান দাও", "লিখে দাও",
    "answer", "solve", "ans dao", "uttor dao",
]


def is_cq_question(message: str) -> bool:
    """
    message-এ অন্তত ২টা (ক)/(খ)/(গ)/(ঘ) marker থাকলে এটা CQ question
    ধরে নেওয়া হয় (একটা থাকলে সাধারণ আলোচনাও হতে পারে)।
    """
    if not message:
        return False
    matches = _CQ_PART_PATTERN.findall(message)
    return len(set(matches)) >= 2


def wants_direct_answer(message: str) -> bool:
    """ছাত্র সরাসরি উত্তর/সমাধান চাইছে কিনা — শুধু তথ্যের জন্য, prompt একই থাকবে।"""
    msg = (message or "").lower()
    return any(k.lower() in msg for k in _ANSWER_REQUEST_KEYWORDS)


# ── CQ marks/structure reference (board rule অনুযায়ী) ──────────
CQ_PART_INFO = {
    "ক": {"name": "জ্ঞানমূলক", "marks": 1,
          "guide": "সরাসরি সংজ্ঞা/তথ্য — ১-২ লাইনে সরল উত্তর। উদ্দীপক লাগে না।"},
    "খ": {"name": "অনুধাবনমূলক", "marks": 2,
          "guide": "'কেন'/ব্যাখ্যা ধরনের — কারণ + ১টা উদাহরণ দিয়ে ৪-৫ লাইন। উদ্দীপক optional।"},
    "গ": {"name": "প্রয়োগমূলক", "marks": 3,
          "guide": "উদ্দীপকের তথ্য সরাসরি প্রয়োগ করে সমাধান — সূত্র/হিসাব + ছোট ব্যাখ্যা, ৬-৮ লাইন। উদ্দীপক বাধ্যতামূলক।"},
    "ঘ": {"name": "উচ্চতর দক্ষতা (মূল্যায়ন)", "marks": 4,
          "guide": "উভয় দিক (যেমন সুবিধা/অসুবিধা বা মতামত) আলোচনা করে শেষে নিজের সিদ্ধান্ত — ১০-১২ লাইন। উদ্দীপক + পাঠ্যবই জ্ঞান দুটোই লাগে।"},
}


def cq_lesson_prompt(class_label: str, subject_bn: str) -> str:
    """
    Lesson Mode + CQ question detect হলে এই prompt ব্যবহার হবে।
    সরাসরি উত্তর না দিয়ে, প্রতিটা part (ক/খ/গ/ঘ) ধরে ধরে marks ও
    structure বুঝিয়ে, ছাত্রকে নিজে লিখতে সাহায্য করবে।
    """
    parts_summary = "\n".join(
        f"  - ({k}) {v['name']} — {v['marks']} মার্ক: {v['guide']}"
        for k, v in CQ_PART_INFO.items()
    )

    return (
        f"তুমি {class_label} শ্রেণীর {subject_bn} বিষয়ের একজন CQ (সৃজনশীল প্রশ্ন) লেখার "
        f"কোচ — Lesson Mode-এ আছো।\n\n"
        f"ছাত্র একটা CQ (উদ্দীপক + ক/খ/গ/ঘ) পাঠিয়েছে এবং উত্তর জানতে চাইছে। "
        f"তোমার কাজ সরাসরি উত্তর লিখে দেওয়া নয় — বরং প্রতিটা অংশ কীভাবে লিখলে "
        f"পুরো marks পাওয়া যায়, তা ধাপে ধাপে শিখিয়ে ছাত্রকে নিজে লিখতে সাহায্য করা।\n\n"
        f"প্রতিটা অংশের marks ও কাঠামো (board rule):\n{parts_summary}\n\n"
        f"নিয়মাবলি:\n"
        f"- প্রথমে (ক) থেকে শুরু করো — বলো এটার জন্য কত marks এবং কী লিখলে full marks পাওয়া যাবে, "
        f"তারপর ছাত্রকে নিজে চেষ্টা করতে বলো ('তুমি একবার লিখে দেখো')।\n"
        f"- ছাত্র লিখলে সেটা check করো — ঠিক থাকলে appreciate করে (খ)-এ যাও, ভুল/অসম্পূর্ণ "
        f"হলে কী missing তা বলে আবার চেষ্টা করতে বলো।\n"
        f"- (গ) ও (ঘ)-এ যাওয়ার আগে বলো উদ্দীপকের কোন তথ্যটা কাজে লাগবে, কিন্তু উদ্দীপক থেকে "
        f"সরাসরি copy করে উত্তর দিও না — ছাত্রকে নিজে সংযোগ করতে গাইড করো।\n"
        f"- (ঘ)-এ উভয় দিক/মতামত আলোচনা করতে বলো এবং শেষে নিজস্ব সিদ্ধান্ত লিখতে উৎসাহ দাও।\n"
        f"- প্রতিটা reply ছোট রাখো (৪-৬ লাইন), একসাথে সব অংশ ব্যাখ্যা করবে না।\n"
        f"- ছাত্র যদি সরাসরি 'পুরো উত্তর লিখে দাও' বলে জোর করে, তাহলে মনে করিয়ে দাও যে "
        f"Lesson Mode-এ তুমি শেখাও, লিখে দাও না — চাইলে Lesson Mode বন্ধ করে আবার জিজ্ঞেস করতে পারে।\n"
        f"- সহজ, বন্ধুত্বপূর্ণ বাংলায় কথা বলো।"
    )
